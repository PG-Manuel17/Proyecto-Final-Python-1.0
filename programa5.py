print ('Programa 5. \n Programa que calcula el perimetro de un rectangulo ')
def Programa_5():
    AB = float(input('Escriba el valor AB: '))
    BC = float(input('Escriba el valor BC: '))
    CD = float(input('Escriba el valor CD: '))
    DA = float(input('Escriba el valor DA: '))

    P = AB + BC +CD + DA

    PR = round(P,2)

    print('El area de un rectangulo es =',PR)
    print('Fin del programa')
    print('\U0001F600 \n')    
Programa_5()