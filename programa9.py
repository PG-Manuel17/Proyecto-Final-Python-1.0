    a = float(input('Escribe el valor de a: '))
    b = float(input('Escribe el valor de b: '))
    c = float(input('Escribe el valor de c: '))

    e1 = (4*a) + (3*b)
    e2 = (21*a) - 18 + (8*b) - 5
    e3 = (4*a) + (3*b) + 7
    e4 = (2*a) + (3*b) - (c*5)
    e5 = (2*a) + (3*b) - (c*2)

    print('El resultado de p1 es =',round(e1,2))
    print('El resultado de p2 es =',round(e2,2))
    print('El resultado de p3 es =',round(e3,2))
