print ('Programa 4. \nPrograma que calcula el Area de un triangulo \n')
def Programa_4():
    B = float(input('Escriba el valor de Base: '))
    A = float(input('Escriba el valor de Altura: '))

    Area = (B * A) / 2
    DR = round(Area,2)
    
    print('El Area es de =',DR)
    
    print('Fin del programa')
    print('\U0001F600 \n')
Programa_4()