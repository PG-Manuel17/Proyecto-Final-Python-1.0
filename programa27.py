print('Programa 27. \nPrograma que indica nuemros mayores y menores a 5\n')
def Programa_27():
    Valor = float(input('Escribe el nuemro: '))

    if Valor == 5:
        print('El numero es igual a 5')
            
    elif Valor < 5:
        print('El numero es menor a 5')
            
    else:
        print('El numero es mayor a 5')

    print('Fin del programa')
    print('\U0001F618\n')
Programa_27()