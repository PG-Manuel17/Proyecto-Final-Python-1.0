    g = kg * 1000
    print('La cantidad en gramos es =', g)

    g = float(input('Ingresa la cantidad de g: '))
    kg = g * 0.001
    print('La cantidad en kilogramos es =', kg)

    m = float(input('Ingresa la cantidad de m: '))
