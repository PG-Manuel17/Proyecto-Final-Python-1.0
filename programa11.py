print ('Programa 11. \nPrograma que que resuelva una regla de 3 simple\n')
def Programa_11():
    A = float(input('Escriba el valor A: '))
    B = float(input('Escriba el valor B: '))
    C = float(input('Escriba el valor C: '))

    D = (B*C)/A

    PR = round(D,2)

    print('Resultado =',PR)
    
    print('Fin del programa')
    print('\U0001F600 \n')
Programa_11()