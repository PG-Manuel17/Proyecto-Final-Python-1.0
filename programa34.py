def Programa_34():
    print('Programa 34. \nPrograma que hace un listado impar y par de numeros\n')
    n = 0
    while n <= 15:
        print(n)
        n = n + 1
        
        if n % 2 == 0:
            print('Es impar')

        else:
            print('Es par')

    print('Fin del programa')
    print('\U0001F618\n')
Programa_34()